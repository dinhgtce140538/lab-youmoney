    public boolean OTP(String toEmail) {
        
        String password = "";
        
        Properties props = new Properties();
        String Capital_chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
        
        String numbers = "0123456789";
        
        String values = Capital_chars
                + numbers;

        // Using random method 
        Random rndm_method = new Random();
        
        for (int i = 0; i < 6; i++) {
            // Use of charAt() method : to get character value 
            // Use of nextInt() as it is scanning the value as int 
            password += values.charAt(rndm_method.nextInt(values.length()));
            
        }
        
        props.put("mail.smtp.auth", "true");
        props.put("mail.smtp.host", OTP.MailConfig.HOST_NAME);
        props.put("mail.smtp.starttls.enable", "true");
        props.put("mail.smtp.port", OTP.MailConfig.TSL_PORT);

        // get Session
        Session session = Session.getDefaultInstance(props, new javax.mail.Authenticator() {
            @Override
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(OTP.MailConfig.APP_EMAIL, OTP.MailConfig.APP_PASSWORD);
            }
        });

        // compose message
        try {
            MimeMessage message = new MimeMessage(session);
            message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(toEmail));
            message.setSubject("Welcome to YourMoney");
            message.setText("you are completed to create account YourMoney, Please to comfirm email by enter OTP code: " + password);

            // send message
            Transport.send(message);
            JOptionPane.showMessageDialog(this, "Message sent successfully");
            while (true) {
                
                String str = JOptionPane.showInputDialog(this, "Enter OTP code");
                if (str.equalsIgnoreCase(password)) {
                    return true;
                } else {
                    JOptionPane.showMessageDialog(this, "Wrong OTP");
                }
            }
        } catch (MessagingException e) {
            System.out.println(e);
        }
        return false;
    }
    
