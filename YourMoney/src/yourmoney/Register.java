    public boolean checkUser(String user, String pass, String mail, String gend, String addre, String nm, String phone, String dob) {
        CheckInput check = new CheckInput();
        int counterror = 0;
        
        if (user.isEmpty()) {
            lblDupUsername.setText("Username cannot null!!!");
            counterror += 1;
        }
        
        if (mail.isEmpty()) {
            lblDupEmail.setText("Email cannot null!!!");
            counterror += 1;
        } else if (check.inputEmail(mail) == null) {
            lblDupEmail.setText("Email dose not exits!!!");
            counterror += 1;
        }
        if (pass.isEmpty()) {
            lblWrongPass.setText("Password cannot null!!!");
            counterror += 1;
        }
        if (gend.isEmpty()) {
            lblNonGender.setText("Please tick gender!!!");
            counterror += 1;
        }
        if (addre.isEmpty()) {
            lblNullAddress.setText("Address cannot null!!!");
            counterror += 1;
        }
        if (nm.isEmpty()) {
            lblNullName.setText("Name cannot null!!!");
            counterror += 1;
        }
        if (phone.isEmpty()) {
            lblNullPhone.setText("Number Phone cannot null!!!");
            counterror += 1;
        } else if (check.inputPhone(phone) == null) {
            lblNullPhone.setText("Number phone dose not exits!!");
            counterror += 1;
        }
        if (!check.inputDOB(dob)) {
            lblWrongDOB.setText("Invalid Date!!!");
        }
        if (counterror > 0) {
            return false;
        }
        
        return true;
    }
    
    public static String encoder(String password) {
        //create varable to store password be encodered
        String generatedPassword = null;
        
        try {
            MessageDigest md = MessageDigest.getInstance("MD5");
            md.update(password.getBytes());
            byte[] bytes = md.digest();
            StringBuilder sb = new StringBuilder();
            for (int i = 0; i < bytes.length; i++) {
                sb.append(Integer.toString((bytes[i] & 0xff) + 0x100, 16).substring(1));
            }
            generatedPassword = sb.toString();
            
        } catch (NoSuchAlgorithmException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
        return generatedPassword;
    }
