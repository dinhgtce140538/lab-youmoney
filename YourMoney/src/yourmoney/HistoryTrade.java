    public void LoadHistory() {

        String username = Login.getUsername();
        Connection conn;
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            conn = DriverManager.getConnection("jdbc:sqlserver://MSI; databaseName=YourMoney;", "sa", "123");
            PreparedStatement st = conn.prepareStatement("Select * From Trade Where Username=?");
            st.setString(1, username);
            ResultSet re = st.executeQuery();
            DefaultTableModel model = (DefaultTableModel) tbHistory.getModel();
            while (re.next()) {
                Object obj[] = {re.getInt("No"), re.getString("Service"), re.getString("Date"), re.getString("Total"),re.getString("Note")};
                model.addRow(obj);
            }
        } catch (SQLException ex) {
            Logger.getLogger(HistoryTrade.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(HistoryTrade.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

