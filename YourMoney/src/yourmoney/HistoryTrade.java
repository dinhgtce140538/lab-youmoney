    private void itemReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemReportActionPerformed
        DefaultTableModel model = (DefaultTableModel) tbHistory.getModel();
        FileOutputStream fos = null;
        BufferedOutputStream bos = null;
        XSSFWorkbook exporttable = null;
        int selectedfile = jFileChooser1.showSaveDialog(null);
        if (selectedfile == JFileChooser.APPROVE_OPTION) {
            exporttable = new XSSFWorkbook();

            XSSFSheet sheet = exporttable.createSheet("Report User");
            String[] tital = {"Trade No", "Payment", "Time", "Money", "Note"};
            XSSFCell cell = null;
            XSSFRow row = sheet.createRow(0);
            for (int i = 0; i < model.getColumnCount(); i++) {
                cell = row.createCell(i);
                cell.setCellValue(tital[i]);
            }
            for (int i = 1; i < model.getRowCount(); i++) {
                System.out.println(i);
                row = sheet.createRow(i);
                for (int j = 0; j < model.getColumnCount(); j++) {
                    cell = row.createCell(j);
                    cell.setCellValue(model.getValueAt(i, j).toString());
                }
            }

            try {
                fos = new FileOutputStream(jFileChooser1.getSelectedFile());
                bos = new BufferedOutputStream(fos);
                exporttable.write(bos);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                try {
                    if (bos != null) {
                        bos.close();
                    }
                    if (fos != null) {
                        fos.close();
                    }

                    if (exporttable != null) {
                        exporttable.close();
                    }
                } catch (IOException ex) {
                    Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_itemReportActionPerformed

